    private void update_table(){

        int c;
        
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/automart","root","");
            String sql="SELECT name,date,phone,rego,mail,amount,status FROM register";
            insert=con.prepareStatement(sql);
            ResultSet rs=insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c=7;
            
            DefaultTableModel df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
            Vector v2=new Vector();
            
                for(int a=1;a<=c;a++){
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("date"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("rego"));
                    v2.add(rs.getString("mail"));
                    v2.add(rs.getString("amount"));
                    v2.add(rs.getString("status"));
                    
                   
                }
                
                df.addRow(v2);
            }
            
            con.close();
            
            
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(this,"record not added");
        
            
        }
        

    }
   
        
        
    
    
